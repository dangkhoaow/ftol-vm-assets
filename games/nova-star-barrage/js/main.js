  function _applyEndlessEnemyModifiers(enemy) {
    if (!isEndlessMode()) return;
    var wave = waveSpawner ? waveSpawner.waveNumber : 0;
    if (wave < 30) return;
    var endCfg = cfg.ENDLESS_MODE;
    // Regen chance
    if (Math.random() < endCfg.regenChance) {
      enemy._hasRegen = true;
      enemy._regenAmount = endCfg.regenAmount;
    }
    // Volatile chance
    if (Math.random() < endCfg.volatileChance) {
      enemy._isVolatile = true;
    }
  }

