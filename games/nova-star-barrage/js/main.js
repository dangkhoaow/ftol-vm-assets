  function _applyChallengeRules() {
    if (!selectedChallenge || !selectedChallenge.rules) return;
    var rules = selectedChallenge.rules;
    // Apply weapon slots restriction
    if (rules.weaponSlots !== undefined && weaponManager) {
      weaponManager.maxWeaponSlots = Math.min(weaponManager.maxWeaponSlots, rules.weaponSlots);
    }
    // Apply stat multipliers
    if (playerEntity) {
      if (rules.hpMultiplier !== undefined) {
        playerEntity.maxHp = Math.floor(playerEntity.maxHp * rules.hpMultiplier);
        playerEntity.hp = playerEntity.maxHp;
      }
      if (rules.attackMultiplier !== undefined) {
        playerEntity.applyStatModifiers([{ stat: 'attack', op: 'multiply', value: rules.attackMultiplier - 1 }]);
      }
      if (rules.speedMultiplier !== undefined) {
        playerEntity.applyStatModifiers([{ stat: 'speed', op: 'multiply', value: rules.speedMultiplier - 1 }]);
      }
      if (rules.pickupRangeBonus !== undefined) {
        playerEntity.applyStatModifiers([{ stat: 'pickupRange', op: 'add', value: rules.pickupRangeBonus }]);
      }
      if (rules.defense !== undefined) {
        playerEntity.stats.defense = rules.defense;
      }
      if (rules.regenPerSec !== undefined) {
        playerEntity._challengeRegen = rules.regenPerSec;
      }
    }
    // Apply time scale
    if (rules.timeScale !== undefined) {
      game.timeScale = rules.timeScale;
    }
    // Apply game-level multipliers
    if (rules.enemyHpMultiplier !== undefined) {
      game.hpMultiplier = (game.hpMultiplier || 1) * rules.enemyHpMultiplier;
    }
    if (rules.enemyCountMultiplier !== undefined) {
      game.__enemyCountMultiplier = rules.enemyCountMultiplier;
    }
    if (rules.dropRateMultiplier !== undefined) {
      game.dropMultiplier = (game.dropMultiplier || 1) * rules.dropRateMultiplier;
    }
    if (rules.xpMultiplier !== undefined) {
      game.xpMultiplier = (game.xpMultiplier || 1) * rules.xpMultiplier;
    }
  }

