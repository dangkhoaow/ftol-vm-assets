  // ============ G2: Daily Challenge ============
  function getDailySeed() {
    var today = new Date().toISOString().slice(0, 10);
    var h = 0;
    for (var i = 0; i < today.length; i++) {
      h = ((h << 5) - h) + today.charCodeAt(i);
      h |= 0; // Convert to 32bit integer
    }
    return Math.abs(h);
  }

  function checkDailyCompleted() {
    try {
      var today = new Date().toISOString().slice(0, 10);
      return localStorage.getItem('ftol:novastarbarrage:DailyRun_' + today) === '1';
    } catch (e) { return false; }
  }

  function markDailyCompleted() {
    try {
      var today = new Date().toISOString().slice(0, 10);
      localStorage.setItem('ftol:novastarbarrage:DailyRun_' + today, '1');
      // 1.5x star coin bonus for daily challenge
      if (window.UpgradeManager && typeof window.UpgradeManager.addStarCoins === 'function') {
        var bonusCoins = Math.floor(cfg.PERMANENT_UPGRADES.starCoinFormula(game.gameTime / 60000, game.kills, bossKillsThisRun) * 0.5);
        window.UpgradeManager.addStarCoins(bonusCoins);
        if (ui) ui.showToast('📅 每日奖励: +' + bonusCoins + '⭐ 额外星币!', 3000, '#ffdd44');
      }
    } catch (e) {}
  }

  // ============ G3: Seed System ============
  function _getSeedFromURL() {
    try {
      var params = new URLSearchParams(window.location.search);
      var seedStr = params.get('seed');
      if (seedStr) {
        var parsed = parseInt(seedStr, 10);
        if (!isNaN(parsed)) return parsed;
        // Try hash of string
        var h = 0;
        for (var i = 0; i < seedStr.length; i++) { h = ((h << 5) - h) + seedStr.charCodeAt(i); h |= 0; }
        return Math.abs(h);
      }
    } catch (e) {}
    return null;
  }

  function _initSeed() {
    if (game._seed !== undefined) return; // already set (daily or URL param)
    var urlSeed = _getSeedFromURL();
    if (urlSeed !== null) {
      game._seed = urlSeed;
    } else {
      game._seed = Date.now() % 2147483647;
    }
    // Mark as auto-generated so restart can use same seed
    game._seedAutoGenerated = true;
    _resetSeedRandom(game._seed);
  }

  // Simple mulberry32 PRNG
  function _resetSeedRandom(seed) {
    var s = seed;
    game._seedRandom = function() {
      s |= 0; s = s + 0x6D2B79F5 | 0;
      var t = Math.imul(s ^ s >>> 15, 1 | s);
      t = t + Math.imul(t ^ t >>> 7, 61 | t) ^ t;
      return ((t ^ t >>> 14) >>> 0) / 4294967296;
    };
    game._seedCurrent = s;
  }

  // Override Math.random with seeded version when seed is active
  var _originalRandom = Math.random;
  function _useSeededRandom() {
    Math.random = function() { return game._seedRandom(); };
  }
  function _restoreRandom() {
    Math.random = _originalRandom;
  }

