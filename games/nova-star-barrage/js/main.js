  // A5: Reroll shop
  function _rerollShop() {
    var state = window._shopState;
    var cost = state.rerollCost;
    if (inRunGold < cost) {
      ui.showToast('金币不足！需要 ' + cost + ' 💰', '#ff4444');
      return;
    }
    inRunGold -= cost;
    state.rerollCount++;
    state.rerollCost = Math.min(state.rerollMaxCost, state.rerollCost * 2);

    // Regenerate items, preserving locked ones
    var isWave = state.waveShopMode;
    _generateAndMergeShopItems(isWave);
    _renderShopItems();

    if (window.audio) window.audio.playPickup();
  }

  // A6: Toggle lock on an item slot
  function _toggleLockItem(itemIndex) {
    var state = window._shopState;
    if (itemIndex < 0 || itemIndex >= state.items.length) return;
    var item = state.items[itemIndex];
    if (item.sold) return;
    if (item.locked) {
      // Unlock
      item.locked = false;
      var liIdx = state.lockedIndices.indexOf(itemIndex);
      if (liIdx >= 0) state.lockedIndices.splice(liIdx, 1);
    } else {
      // Lock — max 2 locked items
      if (state.lockedIndices.length >= 2) {
        ui.showToast('最多锁定 2 个物品！', '#ffaa00');
        return;
      }
      item.locked = true;
      state.lockedIndices.push(itemIndex);
    }
    _renderShopItems();
  }

